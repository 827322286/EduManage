package org.jit.sose.entity;

import lombok.Data;

/**
 * excel合并信息
 * 
 * @author: 王越
 * @date: 2019-10-24 17:10:01
 */
@Data
public class ExcelMerge {

	public ExcelMerge() {
		super();
	}

	public ExcelMerge(Integer row, Integer col, Integer rowspan, Integer colspan) {
		super();
		this.row = row;
		this.col = col;
		this.rowspan = rowspan;
		this.colspan = colspan;
	}

	/**
	 * 行
	 */
	private Integer row;

	/**
	 * 列
	 */
	private Integer col;

	/**
	 * 行合并
	 */
	private Integer rowspan;

	/**
	 * 列合并
	 */
	private Integer colspan;

	private boolean removed;

}
