package org.jit.sose.entity.vo;

import java.util.List;

import lombok.Data;

/**
 * 课程大纲树形结构
 * 
 * @author: 王越
 * @date: 2019-08-01 09:39:39
 */
@Data
public class CourseOutlineTreeVo {

	/**
	 * 节点id
	 */
	private String id;

	/**
	 * 指定节点标签为节点对象的某个属性值
	 */
	private String label;

	/**
	 * 子节点列表
	 */
	private List<AssessmentTreeVo> children;

	/**
	 * 树层级别
	 */
	private Integer treeLevel;
	
	/**
	 * 版本号(域)
	 */
	private String outlineNo;
	
	/**
	 * 版本号名称
	 */
	private String outlineNoName;

	/**
	 * 课程大纲类别标识
	 */
	private Integer courseOutlineTypeId;
	
	/**
	 * 课程大纲类别名称
	 */
	private String typeName;

	/**
	 * 课程信息标识
	 */
	private Integer courseInfoId;
	
	/**
	 * 课程信息名称
	 */
	private String courseName;

	/**
	 * 大纲名称
	 */
	private String outlineName;

	/**
	 * 文件标识
	 */
	private Integer fileInfoId; 
	
	/**
	 * 文件名称
	 */
	private String fileName;

	/**
	 * 访问名称
	 */
	private String accessUrl;
	
	private String remark;

}
